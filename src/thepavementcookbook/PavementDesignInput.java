package thepavementcookbook;

public class PavementDesignInput {
    private String standard;
    private int MSA;

    // Getters and Setters
    public String getStandard() {
        return standard;
    }

    public void setStandard(String standard) {
        this.standard = standard;
    }

    public int getMSA() {
        return MSA;
    }

    public void setMSA(int MSA) {
        this.MSA = MSA;
    }

}
